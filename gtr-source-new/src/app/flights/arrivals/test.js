const blockFilter = "arrival"

function reorderFlights() {
  const now = new Date();
  // Separate flights based on arrival hour (in UTC)
  const currentHourFlights = [];
  const nextHourFlights = [];
  const otherFlights = [];
  for (const flight of data) {
    if (flight[blockFilter].scheduled) {
      const arrivalTime = new Date(flight[blockFilter].scheduled);
      const arrivalTimeUTC = new Date(arrivalTime.getUTCFullYear(), arrivalTime.getUTCMonth(), arrivalTime.getUTCDate(), arrivalTime.getUTCHours(), arrivalTime.getUTCMinutes(), arrivalTime.getUTCSeconds(), arrivalTime.getUTCMilliseconds());
      const arrivalHourUTC = arrivalTimeUTC.getHours();
      const currentHourUTC = now.getHours();
      // console.log(arrivalTime, arrivalTimeUTC,arrivalHourUTC ,currentHourUTC)
      const nextHourUTC = (currentHourUTC + 1) % 24; // Handle hour rollover

      if (arrivalHourUTC === currentHourUTC) {
        currentHourFlights.push(flight);
      } else if (arrivalHourUTC === nextHourUTC) {
        nextHourFlights.push(flight);
      } else {
        otherFlights.push(flight);
      }
    } else {
      otherFlights.push(flight);
    }
  }

  // Sort flights within each category (optional)
  currentHourFlights.sort((flightA, flightB) => {
    const arrivalTimeA = new Date(flightA[blockFilter].scheduled);
    const arrivalTimeB = new Date(flightB[blockFilter].scheduled);
    return arrivalTimeA.getMinutes() - arrivalTimeB.getMinutes();
  });

  nextHourFlights.sort((flightA, flightB) => {
    const arrivalTimeA = new Date(flightA[blockFilter].scheduled);
    const arrivalTimeB = new Date(flightB[blockFilter].scheduled);
    return arrivalTimeA.getMinutes() - arrivalTimeB.getMinutes();
  });

  // Combine and return the sorted arrays
  return [...currentHourFlights, ...nextHourFlights, ...otherFlights];
}




